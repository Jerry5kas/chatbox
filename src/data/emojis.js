export function searchEmojis(query) {
  const results = []
  const searchTerm = query.toLowerCase()
  
  Object.entries(emojiCategories).forEach(([categoryKey, category]) => {
    category.emojis.forEach(emoji => {
      // Simple search - you could enhance this with emoji names/descriptions
      if (emoji.includes(searchTerm) || category.name.toLowerCase().includes(searchTerm)) {
        results.push({ emoji, category: categoryKey })
      }
    })
  })
  
  return results.slice(0, 50) // Limit results
}
